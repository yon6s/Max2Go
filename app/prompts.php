3. 对记录沉淀：还缺哪条一线证据。

**可复制跟进微信/追问话术**
写一段 80-160 字的微信，围绕客户真实关注点推进下一步，不要承诺未审批优惠。

**可沉淀经验**
- 总结 1-3 条可进入案例库的经验，例如客户类型、常见卡点、有效跟进方式、流失预警信号。
- 经验要能复用到下一次相似客户，不要只是复述本单。

**风险提醒**
- 提醒业务员哪些话不能承诺，哪些信息要补齐。

