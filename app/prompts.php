**汇报偏差/被忽视细节**
- 对比业务员主观判断和原始记录，指出可能被忽视、被简化或被误判的地方。
- 如果没有明显偏差，说明“当前汇报与原始记录基本一致”。

**成交/流失关键原因**
- 区分真卡点、试探性压价、流程阻碍、外部不可控因素。
- 如果已成交/已流失，说明决定性原因；如果仍在推进，说明最可能决定成败的 1-2 个因素。

**意向等级**
- 给出等级、理由、升级条件、降级风险。

**下一步动作**
1. 对客户：24小时内要发什么、问什么、约谁。
