{
    $customer = trim((string)($payload['customer']['name'] ?? '客户'));
    $area = trim((string)($payload['customer']['area'] ?? '待确认'));
    $budget = trim((string)($payload['customer']['budget'] ?? '待确认'));
    $moveIn = trim((string)($payload['customer']['moveIn'] ?? '待确认'));

    $base = <<<TEXT
**客户概况**
- 客户：{$customer}
- 需求面积：{$area}
- 预算：{$budget}
- 计划入驻：{$moveIn}

TEXT;

    $results = [
        'tour' => <<<TEXT
