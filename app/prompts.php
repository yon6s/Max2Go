{$base}
**评分/判断**
- 客户意向非常明确，且带了财务，说明成本是底线，但“前台体面”是加分项。
- 竞品（中集美兰城）价格可能较低，我们需要用“综合性价比+园区形象”来打。

**推荐动作**
1. 主推：420㎡形象均衡版（保留大前台和双面采光，单价稍高）。
2. 备胎：380㎡极致成本版（放弃部分采光，控制总价）。
3. 竞品防坑：不要直接贬低竞品，强调我们“得房率高、没有隐形公摊、物业服务响应极快”。

**杀手锏话术**
“您带了财务来，肯定是要算精细账的。我今天不仅给您看这套形象最好的 420 平，还给您备了一套性价比极高的 380 平。咱们不只看单价，等下带您回办公室，我把两套房加物业空调的‘真实总成本’拉个表给您比一比，绝对让您心里有数。”
TEXT,
        'recap' => <<<TEXT
### 复盘结论
{$base}
客户对楼栋形象、采光和前台展示有正反馈，说明项目价值已经建立。当前真卡点是价格优化、装修交付能否赶上 6 月，以及付款周期是否方便财务通过。建议按 B+ 意向推进，若老板二次到访并确认最终面积，可升为 A。

**客户关注点**
- 楼栋形象、采光和前台展示效果。
- 会议室数量是否满足老板要求。
- 价格是否还有优化空间。
- 装修和交付时间是否能赶上 6 月。
- 财务会重点看付款周期。

**客户异议/卡点**
- 真卡点：价格、交付时间、付款周期。
- 试探性压价：客户已经认可形象和采光，价格异议更像是在争取条件，不是完全否定项目。

**意向等级**
- B+：客户有明确正反馈，也愿意约老板再看，但决策人尚未最终确认。
- 升级条件：老板到场、最终面积确认、交付节点能说清。
- 降级风险：竞品用低单价抢客，或交付时间无法匹配客户 6 月入驻节奏。

**下一步动作**
1. 24 小时内发送主推方案、总成本测算和交付节点表。
2. 二次沟通必须约老板或财务一起参与，重点谈面积、付款周期和签约节奏。
3. 内部提前准备可上报的价格/免租期口径，不要先口头承诺。

**可复制跟进微信**
“陈总，今天您主要关注价格、交付时间和老板这边的形象需求。我先把主推面积、总成本和装修进场节点整理成一版，方便您直接给老板和财务看。我们也可以再约一次老板一起到现场，把前台、会议室和最终面积一次定清楚。”

**风险提醒**
- 老板未到场前，不建议承诺特殊优惠。
- 财务关注付款周期，需区分标准条件和可审批条件。
- 交付时间要按真实节点表达，避免承诺“肯定赶得上”。
TEXT,
        'objection' => <<<TEXT
### 异议处理：咬住单价，用免租期和首付换快签
{$base}
**评分/判断**
- 客户开始嫌贵、并且要“回去请示老板”，说明基本看中，但在探底线或找台阶。
- 我方策略是用“综合成本”和“今天定”来交换让步。

**推荐动作**
1. 应对“嫌单价贵”：拆解账本，把单价差异摊到每天/每个人头上，强调我们免空调超时费、得房率高。
2. 应对“请示老板”：给业务员一个“限时特批”的台阶去逼单。

**实战话术（复制发微信）**
“陈总，单价这边我们确实已经是园区底线了，这个您多对比几家肯定能感觉到。与其在单价上抠这几毛钱，不如我帮您去跟总监要个‘特批政策’：只要您能定在本周签约，我去帮您申请多加半个月免租期，等于直接省了几万块现金流，您拿这个方案去跟老板汇报，肯定好批复。”

**向上申请理由（发给总监）**
“客户资质优质，看中 420 平，但卡在单价 2.3，竞品在拉客。我想咬死 2.3 不放，但申请多给半个月免租期作为台阶，争取明天直接落位。”
TEXT,
        'contract' => <<<TEXT
### 合同建议：先生成业务条款清单，再交法务复核
{$base}
**评分/判断**
当前可进入合同草案准备阶段，但面积、免租期、付款日期和特殊条款仍需最终确认。

**推荐动作**
1. 先整理合同替换字段：承租方、房号、面积、租期、单价、物业费、押金、付款周期、免租期。
2. 对特殊条款单独标红：装修支持、提前进场、免租期、付款周期调整。
3. 合同流转前做一次业务自查，减少法务反复修改。

**可复制话术**
“我先按目前确认的商务条件整理合同要素，里面涉及面积、免租期和付款节点的地方会单独标注，最终以双方确认和法务审核版本为准。”

**风险提醒**
- 面积、价格、付款周期在合同正文、附件和报价单中必须一致。
- 未审批的优惠不能写入正式合同。

**下一步**
补齐客户开票信息、签约主体、审批后的商务条件。
TEXT,
        'floorplan' => <<<TEXT
### 平面排布建议：以高坪效和动静分离为主
{$base}
**评分/判断**
- 客户既需要“最大化员工工位”，又要求“老板办公室气派”和“多个封闭会议室”，这在有限面积内会存在一定冲突。
