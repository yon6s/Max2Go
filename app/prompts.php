function stage_prompt(string $stage, array $payload): array
{
    $stageNames = [
        'lead' => '线索与约访',
        'space' => '房源匹配与分割建议',
        'pricing' => '价格测算与报价空间',
        'recap' => '到访录音复盘',
        'proposal' => '客户方案生成',
        'negotiation' => '谈判话术助手',
        'contract' => '合同草案助手',
        'dashboard' => '管理看板与复盘沉淀',
    ];

    $stageName = $stageNames[$stage] ?? '租赁业务辅助';
    $projectKey = (string)($payload['project']['key'] ?? 'general');
    $projectName = (string)($payload['project']['name'] ?? 'MAX科技园（通用）');
    $staticKnowledge = project_knowledge($projectKey);
    $managedKnowledge = knowledge_context($projectKey, $stage);
    $knowledge = trim($managedKnowledge . "\n\n" . $staticKnowledge);
    $json = json_encode($payload, JSON_UNESCAPED_UNICODE | JSON_PRETTY_PRINT);

    $system = <<<PROMPT
你是 MAX科技园资深租赁总监和AI业务助理，帮助一线租赁顾问把客户从获客推进到签约。
输出必须专业、可直接复制给业务员使用，避免空泛口号。
所有建议都要符合商业写字楼/产业园租赁语境，强调可执行、可复盘、可上报。
合同相关内容只生成业务草案和风险提示，不替代法务审核。
当前项目：{$projectName}
必须优先参考项目知识库，但不要逐字照抄；请把项目优势转化成适合客户场景的业务话术。
PROMPT;

    $user = <<<PROMPT
当前模块：{$stageName}

请根据下面的页面输入，生成该模块的结果。要求：
1. 先给出简短结论。
2. 再按小标题输出：评分/判断、推荐动作、可复制话术、风险提醒、下一步。
3. 如果信息不足，请列出需要补充的字段，但仍然基于已有信息给出可用版本。
4. 不要编造具体政策、真实房号、法务结论。
5. 如涉及竞品对抗，用客观比较表达，避免贬损口吻。

项目知识库（优先使用前面的用户维护资料，其次使用内置项目资料）：
{$knowledge}

页面输入：
{$json}
PROMPT;

    return [
        ['role' => 'system', 'content' => $system],
        ['role' => 'user', 'content' => $user],
    ];
}

function demo_stage_result(string $stage, array $payload): string
{
    $customer = trim((string)($payload['customer']['name'] ?? '客户'));
    $area = trim((string)($payload['customer']['area'] ?? '待确认'));
    $budget = trim((string)($payload['customer']['budget'] ?? '待确认'));
    $moveIn = trim((string)($payload['customer']['moveIn'] ?? '待确认'));

    $base = <<<TEXT
**客户概况**
- 客户：{$customer}
- 需求面积：{$area}
- 预算：{$budget}
- 计划入驻：{$moveIn}

TEXT;

    $results = [
        'lead' => <<<TEXT
### 线索判断：建议列为 A 类重点跟进客户
{$base}
**评分/判断**
- 建议评分：86/100。
- 加分项：面积明确、入驻时间紧、决策人可到场、已有竞品对比、重视园区形象。
- 风险点：客户强调总成本，后续谈判大概率会集中在单价、免租期和付款周期。

**推荐动作**
1. 先用“400㎡左右可快速交付”作为主线推进。
2. 准备一套竞品对比口径：楼栋形象、交付确定性、后续扩租、服务响应。
3. 约访时邀请老板或最终拍板人到场，避免只停留在使用部门比较。

**可复制话术**
“您这个需求比较适合先看 400㎡左右、形象和成本相对平衡的房源。我建议这周先把现场看掉，我们会同步准备一版总成本测算，方便您和老板直接比较。”

**风险提醒**
- 不要一开始直接释放最低价格。
- 先确认客户是否已有竞品报价，否则容易被动比价。

**下一步**
约定到访时间，并准备 2 套面积方案和 1 份竞品对比表。
TEXT,
        'appointment' => <<<TEXT
### 约访建议：用“匹配房源+老板决策效率”推动到访
{$base}
**评分/判断**
客户已明确人数、面积、入驻时间和空间配置，约访成熟度高。当前关键不是继续问需求，而是推动客户到现场确认空间感和老板偏好。

**推荐动作**
1. 提前准备 380㎡、420㎡、500㎡三个面积段。
2. 到访前发微信确认：人数、会议室数量、老板办公室、预算边界。
3. 现场带看顺序建议：先看形象均衡房源，再看成本控制房源，最后看扩展弹性房源。

**可复制话术**
“我建议您这周过来一趟，我们直接按 45 人团队给您看两到三种分割方式。这样您不用只看面积数字，可以现场判断前台、会议室和开放办公区放进去是否舒服。”

**风险提醒**
- 客户入驻时间紧，需提前确认房源交付状态。
- 若老板不到场，现场结论可能无法直接推进签约。

**下一步**
发送约访确认微信，并准备客户专属带看单。
TEXT,
        'space' => <<<TEXT
### 房源建议：优先推荐东南侧约 420㎡分割方案
{$base}
**评分/判断**
客户需要 45 人左右办公，400-450㎡更适合兼顾舒适度和总成本。若低于 380㎡，会议室和前台展示会压缩；若超过 500㎡，客户成本压力会增加。

**推荐动作**
1. 主推方案：东南侧采光面约 420㎡，保留前台展示和两间会议室。
2. 备选方案：约 380㎡成本控制版，减少展示区面积。
3. 扩展方案：约 500㎡，预留后续团队扩张和样品/库房空间。

**可复制话术**
“这块建议不要只按最小面积切，您团队 45 人还要会议室和老板办公室，420㎡左右会更稳。我们可以把靠采光面的区域留给开放办公，把入口面做接待和展示。”

**风险提醒**
- 分割方案需要工程确认消防、通道、空调、强弱电和出入口条件。
- 面积最终以合同附件和双方确认口径为准。

**下一步**
把整层平面图标出 A/B/C 三个可分割区，形成带看版本。
TEXT,
        'recap' => <<<TEXT
### 复盘结论：客户意向较高，卡点在价格和交付
{$base}
**评分/判断**
客户现场对楼栋形象和采光认可，说明项目价值已建立。当前主要阻力是总成本、装修交付时间和付款周期，建议意向等级定为 B+，若老板二次到访可升为 A。

**推荐动作**
1. 24 小时内发送三版方案和总成本测算。
2. 单独列出交付节点：确认分割、合同、装修进场、预计入驻。
3. 针对竞品准备对比口径，不直接陷入单价比较。

**可复制话术**
“今天您主要关注价格和交付，我这边先把 420㎡主推方案和总成本拆给您，同时把装修进场时间列清楚。您可以直接拿给老板看，我们再约一次把最终面积定下来。”

**风险提醒**
- 客户财务会关注付款周期，需提前准备标准条件和可上报条件。
- 老板未最终确认前，不建议承诺特殊优惠。

**下一步**
发送客户方案，并约老板或财务参与二次沟通。
TEXT,
        'proposal' => <<<TEXT
### 方案建议：用三套方案帮助客户内部决策
{$base}
**评分/判断**
客户既要控制成本，又重视形象，单一报价容易卡住。建议用“三套方案”让客户自己选择取舍。

**推荐动作**
1. 成本优先版：约 380㎡，控制总价，减少展示区。
2. 形象均衡版：约 420㎡，保留前台、两间会议室、老板办公室。
3. 扩展弹性版：约 500㎡，预留新增团队和样品展示。

**可复制话术**
“我不建议只给您一个面积报价，因为您内部要同时考虑老板形象、员工使用和财务预算。我给您三版，您可以很直观看到每版多出来的钱换到了什么。”

**风险提醒**
- 商务条件要标注“以公司最终审批为准”。
- 平面布局是沟通草案，不作为施工图。

**下一步**
生成客户版 PPT：项目价值、三套面积方案、总成本测算、签约推进节点。
TEXT,
        'negotiation' => <<<TEXT
### 谈判建议：小幅让利要绑定快签和稳定付款
{$base}
**评分/判断**
客户会用竞品价格压价，但同时认可园区形象。谈判重点不是单纯降价，而是把优惠换成可控条件。

**推荐动作**
1. 若客户要求降价：要求明确签约时间和付款周期。
2. 若客户要求免租期：绑定装修进场时间和租期长度。
3. 若客户提竞品：强调形象、交付确定性、服务响应和扩租弹性。

**可复制话术**
“价格我理解您要控制，但我们也要看整体条件。如果您这周能定，并且租期和付款周期按标准推进，我可以帮您把优惠方案向上申请，这样双方都有确定性。”

**风险提醒**
- 不要口头承诺超审批权限的价格和免租期。
- 所有特殊条件都要形成书面上报依据。

**下一步**
让客户确认签约时间、租期、付款周期，再提交优惠申请。
TEXT,
        'contract' => <<<TEXT
### 合同建议：先生成业务条款清单，再交法务复核
{$base}
**评分/判断**
当前可进入合同草案准备阶段，但面积、免租期、付款日期和特殊条款仍需最终确认。

**推荐动作**
1. 先整理合同替换字段：承租方、房号、面积、租期、单价、物业费、押金、付款周期、免租期。
2. 对特殊条款单独标红：装修支持、提前进场、免租期、付款周期调整。
3. 合同流转前做一次业务自查，减少法务反复修改。

**可复制话术**
“我先按目前确认的商务条件整理合同要素，里面涉及面积、免租期和付款节点的地方会单独标注，最终以双方确认和法务审核版本为准。”

**风险提醒**
- 面积、价格、付款周期在合同正文、附件和报价单中必须一致。
- 未审批的优惠不能写入正式合同。

**下一步**
补齐客户开票信息、签约主体、审批后的商务条件。
TEXT,
        'dashboard' => <<<TEXT
### 管理建议：把单个客户推进沉淀为团队打法
{$base}
**评分/判断**
这类 400-500㎡科技研发客户具备较强复制价值，适合形成标准获客、带看、报价和谈判模板。

**推荐动作**
1. 建立“科技研发 400-500㎡”客户标签。
2. 固化三套面积方案和标准话术。
3. 统计丢单原因：价格、交付、装修、竞品、决策周期。

**可复制话术**
“本周这类客户不是没有需求，而是都在比较总成本和交付确定性。建议把可快速交付的 400-500㎡房源先做成推荐包，业务员获客后可以直接调用。”

**风险提醒**
- 管理看板不要只看线索数量，要看每个客户卡在哪个环节。
- 丢单原因要沉淀到话术和房源策略里。

**下一步**
形成部门知识库：成交案例、丢单案例、竞品口径、标准商务条件。
TEXT,
        'project' => <<<TEXT
### 美兰湖项目情报：用“总成本+可落地性+使用体验”对抗竞品
{$base}
**评分/判断**
如果客户正在比较中集美兰城或闵虹项目，美兰湖的核心优势不应只讲租金，而要讲综合使用成本、户型灵活、交付确定性和实际办公体验。客户越关注中小面积、加班、停车、采光和快速入驻，MAX美兰湖优势越明显。

**推荐动作**
1. 对中集美兰城：重点回应户型偏大、分割不灵活、得房率变化、加班空调和停车便利性。
2. 对闵虹·之所智慧方洲：重点回应未开园、签约落地慢、小户型审批、毛坯/标交差异和交付不确定。
3. 对国盛药谷：除非客户主动提及，否则略讲即可，重点回到项目属性和客户适配度。

**可复制话术**
“您现在比较的不是单一租金，而是入驻后的综合成本和确定性。MAX美兰湖这边户型可以按100㎡起灵活分割，物业费、停车费和电费口径也更容易控制；同时项目已运营，可以直接看现场、定分割、推进签约，对您这种希望尽快入驻的客户会更稳。”

**风险提醒**
- 竞品数据对外表达时建议加“约”“以现场为准”，避免说死。
- 不要贬低竞品，用“更适合/不太适合您的需求”来表达。
- 若客户只关心低价，应引导比较物业、空调、电费、停车和装修交付后的总成本。

**下一步**
把客户提到的竞品、关注点和现场反馈记录到客户档案，后续方案中增加一页“综合使用成本与交付确定性对比”。
TEXT,
    ];

    return $results[$stage] ?? $results['lead'];
}
