function stage_prompt(string $stage, array $payload): array
{
    $stageNames = [
        'tour' => '带看实战包 (破冰与推盘)',
        'objection' => '异议处理与逼定',
        'pricing' => '价格测算与报价空间',
        'recap' => '到访录音复盘',
        'contract' => '合同草案助手',
        'floorplan' => 'AI 室内平面设计',
        'video' => '短视频获客助手',
    ];

    $stageName = $stageNames[$stage] ?? '租赁业务辅助';
    $projectKey = (string)($payload['project']['key'] ?? 'general');
    $projectName = (string)($payload['project']['name'] ?? 'MAX科技园（通用）');

    // 仅在关键销售环节引入长篇竞品和项目分析（合同、看板等阶段不需要，以省Token）
    $needsStaticKnowledge = in_array($stage, ['tour', 'objection', 'recap', 'video'], true);
    $staticKnowledge = $needsStaticKnowledge ? project_knowledge($projectKey) : '';
    $managedKnowledge = knowledge_context($projectKey, $stage);
    $knowledge = trim($managedKnowledge . "\n\n" . $staticKnowledge);
    $knowledgeSection = $knowledge ? "项目知识库（优先使用用户维护资料，其次为内置项目资料）：\n{$knowledge}\n\n" : '';

    // 剔除前端传来的空字段与无关元数据（如 csrf, provider），极致减小 JSON 体积
    $inputs = array_filter($payload['inputs'] ?? [], static fn($v) => $v !== '' && $v !== []);
    $cleanPayload = [
        'customer' => $payload['customer'] ?? [],
        'inputs' => $inputs
    ];
    $json = json_encode($cleanPayload, JSON_UNESCAPED_UNICODE | JSON_PRETTY_PRINT);

    $system = <<<PROMPT
你是 MAX科技园资深租赁总监和AI业务助理，帮助一线租赁顾问把客户从获客推进到签约。
输出专业、直接可复制给业务员，避免空泛口号，符合商业办公租赁语境。
当前项目：{$projectName}
必须优先参考项目知识库（如有），将其转化成适合客户场景的业务话术，但不要逐字照抄。
PROMPT;

    $user = <<<PROMPT
当前模块：{$stageName}

请根据页面输入，生成该模块的结果。要求：
1. 先给简短结论。
2. 按小标题输出：评分/判断、推荐动作、可复制话术、风险提醒、下一步。
3. 若信息不足，请列出需补充字段，但仍给出现有信息下的可用建议。
4. 勿编造具体政策、真实房号、法务结论。
5. 如涉及竞品，客观对比，避免贬损。

{$knowledgeSection}页面输入：
{$json}
PROMPT;

    if ($stage === 'recap') {
        $user = <<<PROMPT
当前模块：{$stageName}

你要把“到访录音转文字/带看纪要”复盘成一线租赁顾问能马上执行的跟进动作。要求：
1. 优先从客户原话、现场表现和页面字段中提取事实；不要编造客户没说过的承诺、优惠、房号或政策。
2. 如果转文字很长，先在心里压缩出关键事实，再输出结论；不要复述整段录音。
3. 意向等级用 A / B+ / B / C 标注，并说明升级或降级条件。
4. 微信话术必须像真人销售发给客户的短消息，可直接复制发送，避免官腔。
5. 如果信息不足，仍给出现有信息下的判断，并列出最该补问的 3 个问题。

请按以下小标题输出：

### 复盘结论
用 2-3 句话说明客户真实状态、主要卡点和当前推进判断。

**客户关注点**
- 提取客户最关心的 3-5 个点，尽量引用或转述原话。

**客户异议/卡点**
- 区分“真卡点”和“试探性压价/拖延”。

**意向等级**
- 给出等级、理由、升级条件、降级风险。

**下一步动作**
1. 24小时内要做什么。
2. 二次沟通要约谁、谈什么。
3. 内部需要准备什么材料或政策口径。

**可复制跟进微信**
写一段 80-160 字的微信，围绕客户关注点推进下一步，不要承诺未审批优惠。

**风险提醒**
- 提醒业务员哪些话不能承诺，哪些信息要补齐。

{$knowledgeSection}页面输入：
{$json}
PROMPT;
    }

    return [
        ['role' => 'system', 'content' => $system],
        ['role' => 'user', 'content' => $user],
    ];
}

