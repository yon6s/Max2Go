必须优先参考项目知识库（如有），将其转化成适合客户场景的业务话术，但不要逐字照抄。
PROMPT;

    $user = <<<PROMPT
当前模块：{$stageName}

请根据页面输入，生成该模块的结果。要求：
1. 先给简短结论。
2. 按小标题输出：评分/判断、推荐动作、可复制话术、风险提醒、下一步。
3. 若信息不足，请列出需补充字段，但仍给出现有信息下的可用建议。
4. 勿编造具体政策、真实房号、法务结论。
5. 如涉及竞品，客观对比，避免贬损。

{$knowledgeSection}页面输入：
{$json}
PROMPT;

